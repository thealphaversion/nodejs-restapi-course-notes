// test driven development

// test driven development is writing tests before you write your production code
// and the write the production code

// weird? yes

// it works in the following way

// first we write a failing test
// the test should fail because there is no code to test it

// then write the simplest code to make the test pass

// then refactor code, if necessary

// we repeat these steps over and over, until we have the application ready